package org.instancio.test.properties;

import org.instancio.junit.Given;
import org.instancio.junit.InstancioExtension;
import org.instancio.test.support.tags.Feature;
import org.instancio.test.support.tags.FeatureTag;
import org.instancio.test.support.util.Constants;
import org.junit.jupiter.api.RepeatedTest;
import org.junit.jupiter.api.extension.ExtendWith;

import java.util.HashSet;
import java.util.Set;
import java.util.stream.Collectors;
import java.util.stream.Stream;

import static org.assertj.core.api.Assertions.assertThat;

@FeatureTag(Feature.GLOBAL_SEED)
@ExtendWith(InstancioExtension.class)
class GlobalSeedGivenAnnotationTest {

    private static final Set<String> results = new HashSet<>();

    private @Given Stream<String> streamField;

    @RepeatedTest(Constants.SAMPLE_SIZE_D)
    void field() {
        results.addAll(streamField.limit(10).collect(Collectors.toList()));
        assertThat(results).hasSize(10);
    }
}
